# addition, subtraction, modulo, division, multiplication, exponent

def get_sum(a, b):
  return a + b

def get_diff(a, b):
  return a - b

def get_modulo(a, b):
  remainder = a % b
  return f'The remainder between {a} and {b} is {remainder}'

def get_multi(a, b):
  return a * b

def divide(a, b):
  return a / b

def get_exponent(a, b):
  return a ** b
